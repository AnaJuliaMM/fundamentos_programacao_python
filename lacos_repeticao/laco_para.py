
# INCREMENTO DE 1 EM 1
for i in range(11):
    if (i%2==0):
        print(f"O número {i} é PAR")
    else:
        print(f"O número {i} é ÍMPAR")

print("\n")

# INCREMENTO DE 2 EM 2 DE 10 A 30
for i in range(10,31,2):
    if (i%2==0):
        print(f"O número {i} é PAR")
    else:
        print(f"O número {i} é ÍMPAR")

print("\n")

# DECREMENTO DE 1 EM 1 DE 20 A 0
for i in range(20,-1, -1):
    if (i%2==0):
        print(f"O número {i} é PAR")
    else:
        print(f"O número {i} é ÍMPAR")